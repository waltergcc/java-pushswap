public class Info
{
	public int number;
	public int index;
	public int pos;
	public int whereFit;
	public int movesInA;
	public int movesInB;

	public Info(int number, int index, int pos, int whereFit, int movesInA, int movesInB)
	{
		this.number = number;
		this.index = index;
		this.pos = pos;
		this.whereFit = whereFit;
		this.movesInA = movesInA;
		this.movesInB = movesInB;
	}
}