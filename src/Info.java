public class Info
{
	public int number;
	public int index;
	public int pos;
	public int whereFit;
	public int movesInA;
	public int movesInB;

	public Info(int number)
	{
		this.number = number;
		this.index = 0;
		this.pos = -1;
		this.whereFit = -1;
		this.movesInA = -1;
		this.movesInB = -1;
	}
}